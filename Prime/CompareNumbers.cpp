void path(int O, int P, int Q, int R)
{
	if (O == Q && P == R)
		cout << "Here" << endl;

	if (P < R)
		cout << "North" << endl;
	else if (P > R)
		cout << "South" << endl;
	if (O < Q)
		cout << "East" << endl;
	else if (O > Q)
		cout << "West" << endl;



}

